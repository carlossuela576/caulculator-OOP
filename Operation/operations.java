package Operation;

public class operations {
    
    public double add(double a, double b){
        double sum = a + b;
        return sum;
    }

    public double subtract(double a, double b){
        double difference = a - b;
        return difference;
    }

    public double  multiply(double  a, double  b){
        double product = a * b;
        return product;
    }

    public double divide(double a, double b){
        double quo = a / b;
        return quo;
    }
}
